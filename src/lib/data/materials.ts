// ============================================================
// Helper Functions
// ============================================================

export function getMaterialBySlug(slug: string): Material | undefined {
  return materials.find((m) => m.slug === slug)
}

export function getMaterialByPath(pathSegments: string[]): Material | undefined {
  const fullPath = pathSegments.join('/')
  return materials.find((m) => m.fullPath === fullPath)
}

export function getChildMaterials(parentSlug: string): Material[] {
  return materials.filter((m) => m.parentSlug === parentSlug)
}

export function getMaterialsByCategory(category: string): Material[] {
  return materials.filter((m) => m.category === category)
}
