export function getIndustryBySlug(slug: string): Industry | undefined {
  return industries.find(i => i.slug === slug && i.isActive)
}

export function getIndustryByPath(pathSegments: string[]): Industry | undefined {
  const fullPath = pathSegments.join('/')
  return industries.find(i => i.fullPath === fullPath && i.isActive)
}

export function getChildIndustries(parentSlug: string): Industry[] {
  return industries.filter(i => i.parentSlug === parentSlug && i.isActive).sort((a, b) => a.sortOrder - b.sortOrder)
}

export function getIndustriesByCategory(category: string): Industry[] {
  return industries.filter(i => i.category === category && i.isActive).sort((a, b) => a.sortOrder - b.sortOrder)
}

export function getTopLevelIndustries(): Industry[] {
  return industries.filter(i => i.depth === 1 && i.isActive).sort((a, b) => a.sortOrder - b.sortOrder)
}

export function getAllActiveIndustries(): Industry[] {
  return industries.filter(i => i.isActive).sort((a, b) => a.sortOrder - b.sortOrder)
}
